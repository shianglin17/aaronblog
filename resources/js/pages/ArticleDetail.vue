<template>
  <div class="container">
    <!-- 返回按鈕 - 只在文章加載完成後顯示 -->
    <div v-if="article && !loading" class="navigation-back">
      <n-button quaternary type="primary" @click="goBack">
        <template #icon>
          <n-icon><ArrowBackOutline /></n-icon>
        </template>
        返回文章列表
      </n-button>
    </div>
    
    <n-spin :show="loading" description="載入中...">
      <!-- 錯誤訊息 -->
      <div v-if="error" class="error-message">
        {{ error }}
      </div>
      
      <!-- 文章內容 -->
      <div v-else-if="article" class="article-detail">
        <h1 class="article-title">{{ article.title }}</h1>
        
        <!-- 文章元數據區塊 -->
        <div class="article-meta-wrapper">
        <div class="article-meta">
          <span class="article-author">
            <n-icon size="16" class="meta-icon"><PersonOutline /></n-icon>
            {{ article.author.name }}
          </span>
          <span class="article-category">
            <n-icon size="16" class="meta-icon"><FolderOutline /></n-icon>
            {{ article.category.name }}
          </span>
          <time :datetime="article.created_at">
            <n-icon size="16" class="meta-icon"><TimeOutline /></n-icon>
            {{ formatDate(article.created_at) }}
          </time>

        </div>
        
        <div class="article-tags" v-if="article.tags && article.tags.length > 0">
          <n-icon size="16" class="meta-icon tag-label"><PricetagOutline /></n-icon>
          <span 
            v-for="tag in article.tags" 
            :key="tag.id" 
            class="article-tag"
          >
            {{ tag.name }}
          </span>
          </div>
        </div>
        
        <div class="article-content markdown-body" v-html="renderedContent"></div>
      </div>
      
      <!-- 空資料提示 -->
      <div v-else class="empty-message">
        找不到文章
      </div>
    </n-spin>
    
  </div>
</template>

<script setup lang="ts">
import { ref, onMounted, computed, defineProps } from 'vue';
import { useRoute, useRouter } from 'vue-router';
import { 
  ArrowBackOutline,
  PersonOutline, 
  TimeOutline, 
  FolderOutline, 
  PricetagOutline 
} from '@vicons/ionicons5';
import { articleApi } from '../api/index';
import type { Article } from '../types/article';
import { formatDate } from '../utils/date';
import { useMarkdown } from '../composables/useMarkdown';

// 定義接收的 props
const props = defineProps<{
  slug?: string;
  id?: number;
}>();

const route = useRoute();
const router = useRouter();
const article = ref<Article | null>(null);
const loading = ref(true);
const error = ref('');
const { renderMarkdown } = useMarkdown();



// 渲染 markdown 內容
const renderedContent = computed(() => {
  if (!article.value?.content) return '';
  return renderMarkdown(article.value.content);
});

onMounted(async () => {
  try {
    // 從 props 獲取文章 ID
    const id = props.id;
    
    if (!id || isNaN(id) || id <= 0) {
      throw new Error('無效的文章 ID');
    }
    
    const response = await articleApi.getById(id);
    article.value = response.data;
  } catch (err) {
    error.value = '無法載入文章，請稍後再試';
    console.error('文章載入錯誤:', err);
  } finally {
    loading.value = false;
  }
});

// 返回上一頁
const goBack = () => {
  router.back();
};
</script>

